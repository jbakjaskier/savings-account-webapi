using System.Net;
using System.Net.Http.Json;
using System.Text.Json;
using Reqnroll;
using Westpac.Evaluation.SavingsAccountCreator.Models;

namespace Westpac.Evaluation.Testings.Integration.SavingsAccountCreator;

[Binding]
public class SavingsAccountStepDefinitions(TestContext context)
{
    private string? _customerNumberInRequest;
    private HttpResponseMessage? _response;

    [BeforeScenario]
    public async Task InitializeContainer()
    {
        // Ensures Docker is up and Migrations are run before the first step
        // We call this here to ensure async initialization is awaited properly
        await context.InitializeAsync();
    }

    [AfterTestRun]
    public static async Task CleanUp(TestContext context)
    {
        // Wipes the Postgres container and network once all features finish
        await context.DisposeAsync();
    }

    #region Background

    [Given(@"the bank account API is running")]
    public async Task GivenTheBankAccountApiIsRunning()
    {
        _response = await context.Client.GetAsync("/health/liveness");
        Assert.Equal(HttpStatusCode.OK, _response.StatusCode);
    }

    #endregion

    #region Given Steps

    [Given(@"a customer with (.*) already exists")]
    [Given(@"a customer exists with customer number ""(.*)""")]
    public async Task GivenACustomerExists(string customerNumber)
    {
        // Create customer in the database
        _customerNumberInRequest = customerNumber;
        await context.CreateCustomer(long.Parse(customerNumber), "John", "Doe");
    }

    [Given(@"a customer with customer number ""(.*)"" already has (.*) active accounts")]
    public async Task GivenCustomerHasActiveAccounts(string customerNumber, int count)
    {
        // Create N accounts for this customer in the DB state
        await context.CreateMultipleAccountsForCustomer(long.Parse(customerNumber), count);
    }

    [Given(@"they have ""(.*)"" savings account")]
    public async Task GivenTheyHaveSavingsAccount(int count)
    {
        // Verification/Setup logic
        await context.CreateMultipleAccountsForCustomer(long.Parse(_customerNumberInRequest!), count);
    }

    [Given(@"the Postgres database is offline")]
    public async Task GivenThePostgresDatabaseIsOffline()
    {
        // Trigger a mock to return a failure or kill a container connection
        await context.TemporarilyDisablePostgres();
    }

    #endregion

    #region When Steps

    [When(@"I submit a request to create a savings account with (.*), (.*) and (.*)")]
    public async Task WhenISubmitARequestToCreateASavingsAccount(string customerNum, string nickname, string branchCode)
    {
        var requestBody = new
        {
            customerNumber = customerNum,
            accountNickName = nickname,
            branchCode,
            accountType = "Savings"
        };

        _response = await context.Client.PostAsJsonAsync("/api/v1/account", requestBody);
    }

    [When(@"I submit a request for customer number ""(.*)"" to create a new account")]
    public async Task WhenISubmitNewAccountRequest(string customerNumber)
    {
        await WhenISubmitARequestToCreateASavingsAccount(customerNumber, "Standard Savings", "0001");
    }

    [When(@"I request all the accounts that the customer holds")]
    public async Task WhenIRequestAllAccounts()
    {
        // Logic for GET /accounts/{customerNumber}
        _response = await context.Client.GetAsync("/api/v1/account?customerNumber=" + _customerNumberInRequest);
    }

    #endregion


    #region Then Steps

    [Then(@"the response status should be ""(.*)""")]
    public void ThenTheResponseStatusShouldBe(string statusCode)
    {
        var parsedStatusCode = int.Parse(statusCode);
        Assert.NotNull(_response);
        Assert.Equal(parsedStatusCode, (int)_response.StatusCode);
    }

    [Then(@"the response should contain an autogenerated account number")]
    public async Task ThenResponseContainsKeys()
    {
        Assert.NotNull(_response);
        _response.EnsureSuccessStatusCode();
        Assert.Contains("accountNumber", await _response.Content.ReadAsStringAsync());
    }

    [Then(@"the account details should be saved in the database table")]
    public async Task ThenAccountSavedInDb()
    {
        // Query DB to verify persistence
        var accountsForCustomer = await context.GetAccountsForCustomer(long.Parse(_customerNumberInRequest!));
        Assert.NotNull(accountsForCustomer);
        Assert.NotEmpty(accountsForCustomer);
    }

    [Then(@"the customer should have not exceeded the maximum number of accounts per customer")]
    public async Task ThenLimitNotExceeded()
    {
        var accountsForCustomer = await context.GetAccountsForCustomer(long.Parse(_customerNumberInRequest!));
        Assert.True(accountsForCustomer.Count <= 5);
    }


    [Then(@"the error field should be (.*)")]
    public async Task ThenTheErrorMessageShouldBe(string errorField)
    {
        Assert.NotNull(_response);
        var parsedErrorField =
            JsonSerializer.Deserialize<ValidationFailure>(await _response!.Content.ReadAsStringAsync());
        Assert.Equal(parsedErrorField.Field.ToString(), errorField);
    }

    [Then(@"the response body should contain ""(.*)"" account items")]
    public async Task ThenResponseContainsItems(int count)
    {
        // Verification logic for JSON array length

        var responseString = await _response!.Content.ReadAsStringAsync();
        var parsedAccountResponse = JsonSerializer.Deserialize<List<AccountResponse>>(responseString);
        Assert.NotNull(parsedAccountResponse);
        Assert.Equal(count, parsedAccountResponse.Count);
    }

